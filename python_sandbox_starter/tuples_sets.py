# A Tuple is a collection which is ordered and unchangeable. Allows duplicate members.
 
# Create tuple
fruits = ('Apples','Oranges','Grapes')

# Single value needs trailing comma
fruits2 = ('Apples',)

# Get value
print(fruits[1])

# Delete tuple
del fruits2

# Get length
print(len(fruits))

